using UnityEngine;
using System.Collections;

public class testGUI : MonoBehaviour
{
	carlObject m_carl = null;

	void Start() {
		GameObject carl = GameObject.Find ("carl");
		m_carl = carl.GetComponent<carlObject> ();
	}

	void Update() {

	}

	void OnGUI(){
		// GUI.Label (new Rect (128, 128, 1024, 128), AndroidPluginManager.GetInstance().label_str);

		if(GUI.Button (new Rect (128, 128, 128, 128), "<size=18>CallDialog</size>") == true) {
			AndroidPluginManager.GetInstance().CallDialog();
		}

		if (GUI.Button (new Rect (256, 128, 128, 128), "<size=18>Swing Spine</size>") == true) {
			this.m_carl.toggleSwing ();
		}

		if(GUI.Button (new Rect (384, 128, 128, 128), "<size=18>Sit</size>") == true) {
			this.m_carl.toggleSit ();
		}

		if (GUI.Button (new Rect (512, 128, 128, 128), "<size=18>Bluetooth</size>") == true) {
			AndroidPluginManager.GetInstance().ConnectBluetooth();
		}
	}
}

